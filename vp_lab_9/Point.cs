namespace vp_lab_9
{
    /// <summary>
    /// Точка
    /// </summary>
    public class Point
    {
        // Координаты точки
        public int x;
        public int y;

        /// <summary>
        /// Конструктор
        /// </summary>
        /// <param name="x">Точка x</param>
        /// <param name="y">точка y</param>
        public Point(int x, int y)
        {
            this.x = x;
            this.y = y;
        }
    }
}
